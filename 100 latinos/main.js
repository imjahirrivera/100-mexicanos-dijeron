function getRandomQuestion() {
    const keys = Object.keys(data);
    return keys[Math.floor(Math.random() * keys.length)];
}

function populateAnswers(question) {
    const answers = data[question].answers;

    // Ordenar las respuestas por votos en orden descendente
    answers.sort((a, b) => b.votes - a.votes);

    // Llenar los span correspondientes con las respuestas
    for (let i = 0; i < answers.length; i++) {
        const spanElement = document.getElementById(`res-${i + 1}`);
        if (spanElement) {
            spanElement.innerText = answers[i].text + " [" + answers[i].votes + "]";
        } else {
            console.error(`No se encontró el elemento con ID res-${i + 1}`);
        }
    }

    // Mostrar la pregunta
    const questionElement = document.getElementById('question');
    if (questionElement) {
        questionElement.innerText = question;
    } else {
        console.error('No se encontró el elemento con ID question');
    }
}

// Ejecutar al cargar la página
document.addEventListener('DOMContentLoaded', () => {
    const randomQuestion = getRandomQuestion();
    populateAnswers(randomQuestion);
});